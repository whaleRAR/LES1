"""задача 2: пользователь вводит время в секундах. Перевести время в часы, минуты и секунды и выведите в формате чч:мм:сс
 Используйте форматирование строк"""

print("здоров, меняю секунды на нормальные часы!)))")
time = int(input("введи число секунд, переведем в человеческий вид!"))
hours = time // 3600
min = ( time - hours * 3600 ) // 60
sec = ( time - hours * 3600 - min * 60 )
print("Время в формате чч:мм:сс : ", hours, min, sec)
